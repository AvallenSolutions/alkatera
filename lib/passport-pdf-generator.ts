    doc.setFont('courier', 'normal');
    doc.setFontSize(8);
    doc.setTextColor(PASSPORT_COLORS.stone400);
    doc.text(`REF: ${data.meta.referenceNumber}`, PAGE_WIDTH - MARGIN, 25, { align: 'right' });
