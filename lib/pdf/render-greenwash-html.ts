// ============================================================================
// MAIN RENDERER
// ============================================================================

export function renderGreenwashHtml(result: AnalysisResult): string {
  const pages: string[] = [];

  // Page 1: Cover
  pages.push(renderCoverPage(result));

  // Page 2: Executive Summary
  pages.push(renderSummaryPage(result, 2));

  // Pages 3–N: One claim per page
  result.claims.forEach((claim, i) => {
    pages.push(renderClaimPage(claim, i + 1, 3 + i));
  });

  // Recommendations page
  const recsPageNum = 3 + result.claims.length;
  if (result.recommendations?.length > 0) {
    pages.push(renderRecommendationsPage(result.recommendations, recsPageNum));
  }

  // About page
  const aboutPageNum = recsPageNum + (result.recommendations?.length > 0 ? 1 : 0);
  pages.push(renderAboutPage(aboutPageNum));

  // Replace __PAGE_NUM__ placeholders with sequential numbers
  let counter = 0;
  const pagesHtml = pages.join('\n').replace(/__PAGE_NUM__/g, () => String(++counter));

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8" />
  <title>Greenwash Guardian Report</title>

  <link rel="preconnect" href="https://fonts.googleapis.com" />
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
  <link href="https://fonts.googleapis.com/css2?family=Inter:wght@300;400;500;600;700&family=Playfair+Display:wght@300;700&family=Fira+Code:wght@400;700&display=swap" rel="stylesheet" />

  <style>
    *, *::before, *::after { margin: 0; padding: 0; box-sizing: border-box; }

    body {
      font-family: 'Inter', system-ui, -apple-system, sans-serif;
      -webkit-font-smoothing: antialiased;
      background: white;
      color: #1c1917;
    }

    @page { size: A4; margin: 0; }

    .page {
      width: 794px;
      height: 1123px;
      position: relative;
      display: flex;
      flex-direction: column;
      padding: 48px;
      padding-bottom: 80px;
      overflow: hidden;
      page-break-after: always;
      break-after: page;
    }

    .page:last-child { page-break-after: auto; break-after: auto; }

    .dark-page { background: #1c1917; color: white; }
    .light-page { background: #f5f5f4; color: #1c1917; }

    /* Content area below header bar */
    .page-body {
      margin-top: 16px;
      flex: 1;
    }

    @media print {
      body { background: white; }
      .page { box-shadow: none; margin: 0; }
    }
  </style>
</head>
<body>
  ${pagesHtml}
</body>
</html>`;
}
