/**
 * Greenwash Guardian PDF — HTML Template
 *
 * Every page is a fixed 794×1123px div (matching the LCA report pattern).
 * Each page has its own header bar, content area, and absolute-positioned footer.
 * This avoids all Chromium page-break issues with flowing content.
 *
 * Page structure:
 * 1. Cover page (dark)
 * 2. Executive Summary (light)
 * 3–N. One claim per page (light)
 * N+1. Recommendations (light) — may span 2 pages
 * N+2. About/CTA (dark)
 */

// ============================================================================
// TYPES
// ============================================================================

interface ClaimResult {
  claim_text: string;
  claim_context?: string;
  risk_level: 'low' | 'medium' | 'high';
  risk_score: number;
  issue_type: string;
  issue_description: string;
  legislation_name: string;
  legislation_article?: string;
  legislation_jurisdiction: 'uk' | 'eu' | 'both';
  suggestion: string;
  suggested_revision?: string;
}

interface AnalysisResult {
  url: string;
  overall_risk_level: 'low' | 'medium' | 'high';
  overall_risk_score: number;
  summary: string;
  recommendations: string[];
  legislation_applied: Array<{
    name: string;
    jurisdiction: 'uk' | 'eu' | 'both';
    key_requirement: string;
  }>;
  claims: ClaimResult[];
}

// ============================================================================
// HELPERS
// ============================================================================

function esc(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function riskColour(level: string): string {
  switch (level) {
    case 'high': return '#dc2626';
    case 'medium': return '#d97706';
    default: return '#16a34a';
  }
}

function riskBg(level: string): string {
  switch (level) {
    case 'high': return '#fef2f2';
    case 'medium': return '#fffbeb';
    default: return '#f0fdf4';
  }
}

function riskBorder(level: string): string {
  switch (level) {
    case 'high': return '#fecaca';
    case 'medium': return '#fde68a';
    default: return '#bbf7d0';
  }
}

function jurisdictionLabel(j: string): string {
  switch (j) {
    case 'uk': return 'UK';
    case 'eu': return 'EU';
    case 'both': return 'UK &amp; EU';
    default: return esc(j);
  }
}

function issueTypeLabel(t: string): string {
  return (t || '').replace(/_/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
}

